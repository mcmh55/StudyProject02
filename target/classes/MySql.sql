DROP TABLE PP1_MEMBER
CASCADE CONSTRAINTS;

DROP SEQUENCE PP1_MEMBER_SEQ;

CREATE TABLE PP1_MEMBER(
	MEMBER_IDX			NUMBER,
	MEMBER_ID			VARCHAR2(12)	NOT NULL,
	MEMBER_PW			VARCHAR2(100)	NOT NULL,
	MEMBER_NICKNAME		VARCHAR2(20)	NOT NULL,
	MEMBER_NAME			VARCHAR2(40)	NOT NULL,
	MEMBER_SEX			NUMBER(1)		NOT NULL,
	MEMBER_BIRTHDAY		VARCHAR2(8)		NOT NULL,
	MEMBER_ADDRESS		VARCHAR2(400)	NOT NULL,
	MEMBER_CHECK_EMAIL	NUMBER(1)		NOT NULL,
	MEMBER_CHECK_SMS	NUMBER(1)		NOT NULL,
	MEMBER_CHECK_PHONE	NUMBER(1)		NOT NULL,
	MEMBER_EMAIL		VARCHAR2(320)	NOT NULL,
	MEMBER_PHONE		VARCHAR2(20)	NOT NULL,
	MEMBER_ACTIVE		NUMBER(1)		NOT NULL,
	MEMBER_AUTH			NUMBER(1)		NOT NULL,
	CONSTRAINT PP1_MEMBER_PK PRIMARY KEY(MEMBER_IDX),
	CONSTRAINT PP1_MEMBER_UK UNIQUE(MEMBER_ID)
);

CREATE SEQUENCE PP1_MEMBER_SEQ
START WITH 1 INCREMENT BY 1;

